// Primitive data types
let number = 10; // Number
let string = "Hello, world!"; // String
let boolean = true; // Boolean
let nullValue = null; // Null
let undefinedValue = undefined; // Undefined

// Advanced data types
let array = [1, 2, 3, 4, 5]; // Array
let object = { name: "John", age: 30, city: "New York" }; // Object
let functionValue = function() { console.log("Hello from a function!"); }; // Function

console.log(number);
console.log(string);
console.log(boolean);
console.log(nullValue);
console.log(undefinedValue);
console.log(array);
console.log(object);
functionValue();