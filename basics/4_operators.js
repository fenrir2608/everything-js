// Arithmetic operators
let num1 = 10;
let num2 = 5;

let addition = num1 + num2; // Addition: 15
let subtraction = num1 - num2; // Subtraction: 5
let multiplication = num1 * num2; // Multiplication: 50
let division = num1 / num2; // Division: 2
let modulus = num1 % num2; // Modulus: 0

// Assignment operators
let x = 10;
x += 5; // Equivalent to x = x + 5. x is now 15.
x -= 3; // Equivalent to x = x - 3. x is now 12.
x *= 2; // Equivalent to x = x * 2. x is now 24.
x /= 4; // Equivalent to x = x / 4. x is now 6.
x %= 2; // Equivalent to x = x % 2. x is now 0.

// Comparison operators
let a = 5;
let b = 10;

let isEqual = a == b; // Equal to: false
let isNotEqual = a != b; // Not equal to: true
let isGreater = a > b; // Greater than: false
let isLess = a < b; // Less than: true
let isGreaterOrEqual = a >= b; // Greater than or equal to: false
let isLessOrEqual = a <= b; // Less than or equal to: true

// Logical operators
let p = true;
let q = false;

let logicalAnd = p && q; // Logical AND: false
let logicalOr = p || q; // Logical OR: true
let logicalNot = !p; // Logical NOT: false

// String concatenation
let str1 = "Hello";
let str2 = "World";

let concatenatedString = str1 + " " + str2; // Concatenation: "Hello World"

// Ternary operator
let age = 18;
let isAdult = age >= 18 ? "Adult" : "Not an adult"; // isAdult: "Adult"

// typeof operator
let variable = 10;
let type = typeof variable; // Returns "number"

// Increment and decrement operators
let count = 5;
count++; // Increment by 1. count is now 6.
count--; // Decrement by 1. count is now 5.

// Bitwise operators (example: bitwise AND)
let num3 = 5;
let num4 = 3;

let bitwiseAnd = num3 & num4; // Returns 1

